package com.example.movieapp;

public class ReviewDetailFragmentMainData {
    private int iv_poster;  //이미지뷰는 int 값임.
    private String tv_name, tv_review;

    //생성자
    public ReviewDetailFragmentMainData() {
        this.iv_poster = 0;
        this.tv_name = null;
        this.tv_review = null;
    }

    //getter
    public int getIv_poster() {
        return iv_poster;
    }
    //setter
    public void setIv_poster(int iv_poster) {
        this.iv_poster = iv_poster;
    }

    public String getTv_name() {
        return tv_name;
    }

    public void setTv_name(String tv_name) {
        this.tv_name = tv_name;
    }

    public String getTv_review() {
        return tv_review;
    }

    public void setTv_review(String tv_review) {
        this.tv_review = tv_review;
    }

}
